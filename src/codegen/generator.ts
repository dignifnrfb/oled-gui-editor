function generatePageRenderCode(page: Page, _config: ProjectConfig): string {
  const lines: string[] = []

  // Background
  if (page.backgroundColor === 1) {
    lines.push('    OLED_Fill(OLED_COLOR_WHITE);')
  }

  // Sort elements by zIndex
  const sortedElements = [...page.elements].sort((a, b) => a.zIndex - b.zIndex)

  for (const element of sortedElements) {
    if (!element.visible) continue

    const color = element.properties.color === 1 ? 'OLED_COLOR_WHITE' : 'OLED_COLOR_BLACK'

    switch (element.type) {
      case 'pixel':
        lines.push(`    OLED_SetPixel(${element.x}, ${element.y}, ${color});`)
        break

      case 'line':
        const x2 = element.x + (element.properties.x2 || 0)
        const y2 = element.y + (element.properties.y2 || 0)
        lines.push(`    OLED_DrawLine(${element.x}, ${element.y}, ${x2}, ${y2}, ${color});`)
        break

      case 'rect':
        // Canvas boundaries include endpoints; export width/height +1 to stay aligned
        const rectW = element.width + 1
        const rectH = element.height + 1
        if (element.properties.fill) {
          lines.push(`    OLED_FillRect(${element.x}, ${element.y}, ${rectW}, ${rectH}, ${color});`)
        } else {
          lines.push(`    OLED_DrawRect(${element.x}, ${element.y}, ${rectW}, ${rectH}, ${color});`)
        }
        break

      case 'circle': {
        const r = Math.floor(element.width / 2)
        const cx = element.x + r
        const cy = element.y + r
        if (element.properties.fill) {
          lines.push(`    OLED_FillCircle(${cx}, ${cy}, ${r}, ${color});`)
        } else {
          lines.push(`    OLED_DrawCircle(${cx}, ${cy}, ${r}, ${color});`)
        }
        break
      }

      case 'roundRect': {
        const rrW = element.width + 1
        const rrH = element.height + 1
        const radius = Math.min(
          element.properties.radius || 4,
          Math.floor(rrW / 2),
          Math.floor(rrH / 2)
        )
        if (element.properties.fill) {
          lines.push(`    OLED_FillRoundRect(${element.x}, ${element.y}, ${rrW}, ${rrH}, ${radius}, ${color});`)
        } else {
          lines.push(`    OLED_DrawRoundRect(${element.x}, ${element.y}, ${rrW}, ${rrH}, ${radius}, ${color});`)
        }
        break
      }

      case 'text':
        if (element.properties.text) {
          const size = element.properties.fontSize || 8
          lines.push(`    OLED_DrawString(${element.x}, ${element.y}, "${element.properties.text}", ${size}, ${color});`)
        }
        break

      case 'image':
        // Image data would need to be exported separately
        lines.push(`    // TODO: Draw image at (${element.x}, ${element.y})`)
        break
    }
  }

  return lines.length > 0 ? lines.join('\n') : '    // Empty page'
}

function generatePageLinkArray(page: Page, pages: Page[]): string {
  const arrayName = `Page_${sanitizeName(page.name)}_Links`

  if (page.links.length === 0) {
    return `static const GUI_PageLink* ${arrayName} = NULL;\nstatic const uint8_t ${arrayName}_LinkCount = 0;`
  }

  const entries = page.links.map(link => {
    const trigger = mapTriggerToEventId(link.trigger)
    const target = findTargetPageEnum(link.targetPageId, pages)
    const transition = mapTransitionToEnum(link.transition)
    return `    { ${trigger}, ${target}, ${transition} }`
  }).join(',\n')

  return `static const GUI_PageLink ${arrayName}[] = {\n${entries}\n};\nstatic const uint8_t ${arrayName}_LinkCount = sizeof(${arrayName}) / sizeof(GUI_PageLink);`
}

function mapTriggerToEventId(trigger: PageLink['trigger']): string {
  switch (trigger) {
    case 'KEY_UP':
      return 'GUI_EVENT_KEY_UP'
    case 'KEY_DOWN':
      return 'GUI_EVENT_KEY_DOWN'
    case 'KEY_LEFT':
      return 'GUI_EVENT_KEY_LEFT'
    case 'KEY_RIGHT':
      return 'GUI_EVENT_KEY_RIGHT'
    case 'KEY_OK':
      return 'GUI_EVENT_KEY_OK'
    case 'KEY_BACK':
      return 'GUI_EVENT_KEY_BACK'
    case 'TIMER':
      return 'GUI_EVENT_TIMER'
    case 'CONDITION':
      return 'GUI_EVENT_CONDITION'
    default:
      return 'GUI_EVENT_NONE'
  }
}

function mapTransitionToEnum(transition: PageLink['transition']): string {
  switch (transition) {
    case 'SLIDE_LEFT':
      return 'GUI_TRANSITION_SLIDE_LEFT'
    case 'SLIDE_RIGHT':
      return 'GUI_TRANSITION_SLIDE_RIGHT'
    case 'FADE':
      return 'GUI_TRANSITION_FADE'
    case 'NONE':
    default:
      return 'GUI_TRANSITION_NONE'
  }
}

function findTargetPageEnum(targetPageId: string, pages: Page[]): string {
  const target = pages.find(p => p.id === targetPageId)
  if (!target) return 'PAGE_COUNT'
  return `PAGE_${sanitizeName(target.name).toUpperCase()}`
}

function generateImagesHeader(elements: Element[], config: ProjectConfig): string {
  const date = new Date().toISOString().split('T')[0]
  const imageDeclarations: string[] = []
  const imageData: string[] = []

  for (let i = 0; i < elements.length; i++) {
    const el = elements[i]
    if (!el.properties.imageData) continue

    const imgName = `img_${sanitizeName(el.id.slice(0, 8))}_${i}`
    const width = el.width
    const height = el.height
    const pages = Math.ceil(height / 8)

    // Generate placeholder byte array (actual conversion would happen at export time)
    // For now, generate a pattern based on element properties
    const byteCount = width * pages
    const bytes: number[] = []

    // If we have actual processed data, use it; otherwise generate placeholder
    if (el.properties.processedData) {
      // Use pre-processed data
      const data = el.properties.processedData as number[]
      bytes.push(...data)
    } else {
      // Generate placeholder pattern
      for (let j = 0; j < byteCount; j++) {
        bytes.push(0x00)
      }
    }

    // Format bytes as C array
    const bytesPerRow = 16
    const byteLines: string[] = []
    for (let j = 0; j < bytes.length; j += bytesPerRow) {
      const rowBytes = bytes.slice(j, Math.min(j + bytesPerRow, bytes.length))
      const hexStr = rowBytes.map(b => `0x${b.toString(16).padStart(2, '0').toUpperCase()}`).join(', ')
      const isLast = j + bytesPerRow >= bytes.length
      byteLines.push(`    ${hexStr}${isLast ? '' : ','}`)
    }

    imageData.push(`/* Image: ${imgName} (${width}x${height}) */
static const uint8_t ${imgName}[] = {
${byteLines.join('\n')}
};
static const uint16_t ${imgName}_width = ${width};
static const uint16_t ${imgName}_height = ${height};
`)

    imageDeclarations.push(`extern const uint8_t ${imgName}[];`)
    imageDeclarations.push(`extern const uint16_t ${imgName}_width;`)
    imageDeclarations.push(`extern const uint16_t ${imgName}_height;`)
  }

  return `/**
 * @file    oled_images.h
 * @brief   OLED Image Data
 * @author  ${config.author || 'OLED GUI Editor'}
 * @date    ${date}
 * @note    Generated by OLED GUI Editor
 *
 * Image format: ${config.codeGen.arrayFormat === 'COLUMN' ? 'Column-major (page mode)' : 'Row-major'}
 * Bit order: ${config.codeGen.bitOrder}
 */

#ifndef __OLED_IMAGES_H
#define __OLED_IMAGES_H

#ifdef __cplusplus
extern "C" {
#endif

#include <stdint.h>

/* Image count: ${elements.length} */

${imageData.join('\n')}

#ifdef __cplusplus
}
#endif

#endif /* __OLED_IMAGES_H */
`
}

// Code generator for OLED GUI Editor
import type { Project, Page, PageLink, Element, ProjectConfig } from '@/types'

export function generateCode(project: Project): { name: string; content: string }[] {
  const files: { name: string; content: string }[] = []
  const config = project.config

  // Generate driver files
  files.push({
    name: 'oled_driver.h',
    content: generateDriverHeader(config)
  })

  files.push({
    name: 'oled_driver.c',
    content: generateDriverSource(config)
  })

  // Generate font file
  files.push({
    name: 'oled_font.h',
    content: generateFontHeader()
  })

  // Generate GUI framework files
  files.push({
    name: 'oled_gui.h',
    content: generateGUIHeader(project)
  })

  files.push({
    name: 'oled_gui.c',
    content: generateGUISource(project)
  })

  // Generate page files
  for (const page of project.pages) {
    const pageName = sanitizeName(page.name)
    files.push({
      name: `page_${pageName}.h`,
      content: generatePageHeader(page, config)
    })
    files.push({
      name: `page_${pageName}.c`,
      content: generatePageSource(page, config)
    })
  }

  // Generate image data if any
  const imageElements = project.pages.flatMap(p =>
    p.elements.filter(e => e.type === 'image')
  )
  if (imageElements.length > 0) {
    files.push({
      name: 'oled_images.h',
      content: generateImagesHeader(imageElements, config)
    })
  }

  return files
}

function sanitizeName(name: string): string {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]/g, '_')
    .replace(/_+/g, '_')
    .replace(/^_|_$/g, '')
}

